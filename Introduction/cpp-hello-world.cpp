#include <cstdio>   // Include cstdio (cstdio) library for C-style input/output operations

using namespace std; // Use the standard namespace to avoid prefixing std::


int main() {
    /*
    The main function is the entry point of any C++ program.
    When you run a C++ program, the execution starts from the main function.
    The reason it is declared as `int` is because it returns an integer value
    to the operating system when the program ends.
    Returning 0 typically means that the program has executed successfully.
    */

    // Print the message "Hello, World!" to the standard output using printf
    // printf is a C-style function for formatted output
    printf("Hello, World!");
    
    // Return 0 to indicate successful program execution
    return 0;
}
